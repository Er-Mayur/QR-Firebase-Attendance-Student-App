package com.ermayurmahajan.mcoestudentapp;

public class ReadWriteDetails {
    public String textStudentFullName, textDOB, textGenderSelected, textYear, textAcademicYear, textRollNo, textBatch, textStudentMobileNumber, textEmail, textParentFullName, textParentMobileNumber;
    public ReadWriteDetails(){}
    public ReadWriteDetails(String textStudentFullName, String textDOB, String textGenderSelected, String textYear, String textAcademicYear, String textRollNo, String textBatch, String textStudentMobileNumber, String textEmail, String textParentFullName, String textParentMobileNumber) {

        this.textStudentFullName = textStudentFullName;
        this.textDOB = textDOB;
        this.textGenderSelected = textGenderSelected;
        this.textYear = textYear;
        this.textAcademicYear = textAcademicYear;
        this.textRollNo = textRollNo;
        this.textBatch = textBatch;
        this.textStudentMobileNumber = textStudentMobileNumber;
        this.textEmail = textEmail;
        this.textParentFullName = textParentFullName;
        this.textParentMobileNumber = textParentMobileNumber;
    }
}